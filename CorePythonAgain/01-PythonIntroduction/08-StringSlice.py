a = "Hello this is python"

print(a[0:5])

# print string from index 6 to 10-1
print(a[6:10])

# Will print 0-20 with a step of 5
print(a[0:20:5])

# Negative Slicing
print(a[-1])

print(a[:])

print(a[0:])

print(a[:10])

print(a[:-1])

print(a[::-1])

print(a[:100])

print(len(a))
